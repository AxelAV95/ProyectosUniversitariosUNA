    public void llenarTablaExtras(){
        NodoExtra actual = Datos.getListaExtras().getPrimero();
        if (Datos.getListaExtras().getPrimero() == null && Datos.getListaExtras().getPrimero() == null) {
            JOptionPane.showMessageDialog(this, "No hay datos de extras.");

        } else {
            while (actual != null) {
                Object[] data = {
                    actual.getExtra().getCodigo(), actual.getExtra().getDescripcion()};
                modeloExtras.addRow(data);

                actual = actual.getSiguiente();
            }
        }
    }

