    public void cargarComboExtras() {

        
        NodoExtra ne = Datos.getListaExtras().getPrimero();
        while (ne != null) {
            comboExtras.addItem(ne.getExtra().getCodigo() + "-" + ne.getExtra().getDescripcion());
            ne = ne.getSiguiente();
        }

    }

  
