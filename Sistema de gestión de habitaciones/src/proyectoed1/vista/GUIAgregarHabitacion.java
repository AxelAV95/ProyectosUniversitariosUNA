    public void limpiarCampos() {
        campoDescripcion.setText("");
        campoPrecio.setText("");
        campoPersonas.setText("");
        modelExtras.setRowCount(0);
    }

    public static boolean verificarTablaVacia(JTable jTable) {
        if (jTable != null && jTable.getModel() != null) {
            return jTable.getModel().getRowCount() <= 0 ? true : false;
        }
        return false;
    }

    public boolean validarCampos() {
        if (campoDescripcion.getText().equals("") || campoPrecio.getText().equals("") || campoPersonas.getText().equals("") || verificarTablaVacia(tablaExtrasHab) == true) {
            return true;
        }
        return false;
    }

    private void agregarHabitacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarHabitacionActionPerformed
        // TODO add your handling code here:

        if (validarCampos() == true) {
            JOptionPane.showMessageDialog(null, "Debe llenar los campos correspondientes.");
        } else {
            Habitacion h = new Habitacion();
            
           
            ListaExtra le = new ListaExtra();

          
            try {
                h.setNumHabitacion(Globales.controladorHabitacion.obtenerNuevoID("habitaciones.txt")+1);
            } catch (IOException ex) {
                Logger.getLogger(GUIAgregarHabitacion.class.getName()).log(Level.SEVERE, null, ex);
            }
            h.setDescripcion(campoDescripcion.getText());
            h.setPrecio(Integer.parseInt(campoPrecio.getText()));
            h.setCantidadPersonas(Integer.parseInt(campoPersonas.getText()));

            Object[] columnData = new Object[tablaExtrasHab.getRowCount()];
            Object[] rowData = new Object[tablaExtrasHab.getRowCount()];
            for (int i = 0; i < tablaExtrasHab.getRowCount(); i++) {
                Extra e = new Extra();
                String[] splitfila
                        = tablaExtrasHab.getValueAt(i, 0).toString().split("-");

                e.setCodigo(splitfila[0]);
                e.setDescripcion(splitfila[1]);
                Globales.controladorExtra.agregar(le, e);
            }
            h.setListaExtras(le);
         
           h.setImagenes(imagenes);
            
           
            try {
                if (Globales.controladorHabitacion.agregar(Datos.getListaHabitaciones(), h) == true && Globales.controladorHabitacion.archivar("habitaciones.txt", h) == true && Globales.controladorHabitacion.guardarImagenes(imagenes, Globales.rutaGeneral, h.getNumHabitacion()) == true) {
                    JOptionPane.showMessageDialog(null, "Agregado con éxito.");
                   
                    Datos.resetearListaHabitaciones();
                    Datos.iniciarListaHabitaciones();
                    limpiarCampos();
                    this.dispose();
                    new GUIHabitaciones().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Error al agregar.");
                }
            } catch (IOException ex) {
                Logger.getLogger(GUIAgregarHabitacion.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_agregarHabitacionActionPerformed

    private void seleccionImgBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seleccionImgBotonActionPerformed
       
        Globales.jc.setDialogTitle("Selecciona las imágenes..");
        Globales.jc.setMultiSelectionEnabled(true);

       
        if (Globales.jc.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            imagenes = Globales.jc.getSelectedFiles();

            if (imagenes.length > 5) {
                JOptionPane.showMessageDialog(null, "No se puede agregar más de 5 imágenes.");
                imagenes = null;
            } else {
                for (File imagen : imagenes) {
                    imagen.getName();

                    JLabel jlabelpic = new JLabel(new ImageIcon(new ImageIcon(imagen.getAbsolutePath()).getImage().getScaledInstance(150, 150, Image.SCALE_DEFAULT)));
                    System.out.print(imagen.getAbsolutePath() + "\n");
                   

                    panelImagenes.add(jlabelpic);
                    contador++;
                     
                     
                    if(contador > 5){
                        seleccionImgBoton.setEnabled(false);
                        
                        imagenes = null;
                        contador = 0;
                    }
                    System.out.print(imagen.getName() + "\n");

                }
   
                revalidate();
                repaint();
              
            }
        }

    }//GEN-LAST:event_seleccionImgBotonActionPerformed

