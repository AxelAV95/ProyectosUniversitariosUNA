    private void campoCedulaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoCedulaKeyPressed
        String value = campoCedula.getText();
        int l = value.length();
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9' || evt.getKeyCode() == evt.VK_BACK_SPACE) {
            campoCedula.setEditable(true);

        } else {
            campoCedula.setEditable(false);
            JOptionPane.showMessageDialog(null, "Solo se permiten números.");
        }
    }//GEN-LAST:event_campoCedulaKeyPressed

