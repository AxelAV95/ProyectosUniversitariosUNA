    private void botonBuscarFechasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarFechasActionPerformed
        modeloReportes.setRowCount(0);
        
        auxReserva= Globales.controladorReserva.obtenerListaFechas(Datos.getListaReservas(), fechaInicio.getDate(), fechaFinal.getDate());
        NodoReserva actual = auxReserva.getPrimero();
        if (auxReserva.getPrimero() == null && auxReserva.getUltimo() == null) {
            JOptionPane.showMessageDialog(this, "No hay datos de reservas.");

        } else {
            while (actual != null) {
                Object[] data = {
                    actual.getReserva().getNumeroReservacion(), crearFechaAString(actual.getReserva().getFechaActual()), crearFechaAString(actual.getReserva().getFechaInicio()), crearFechaAString(actual.getReserva().getFechaFinal()),
                    actual.getReserva().getCliente().getCedula(), actual.getReserva().getCliente().getNombre()};
                modeloReportes.addRow(data);

                actual = actual.getSiguiente();
            }
        }
    }//GEN-LAST:event_botonBuscarFechasActionPerformed

