    private void activarFiltroHabitacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_activarFiltroHabitacionActionPerformed
        // TODO add your handling code here:
        panelImagenes.removeAll();

        if (activarFiltroHabitacion.isSelected()) {

            if (filtroCedula.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "El campo esta vacio");
                filtroCedula.setText("");
            } else if (Globales.controladorHabitacion.verificarHabitacion(Integer.parseInt(filtroCedula.getText()), Datos.getListaHabitaciones()) == false) {
                JOptionPane.showMessageDialog(null, "No existe la habitaci√≥n solicitada.");
                filtroCedula.setText("");
            } else {
                Habitacion h = Globales.controladorHabitacion.obtenerHabitacion(Integer.parseInt(filtroCedula.getText()), Datos.getListaHabitaciones());
                System.out.print(h.getDescripcion());
                //cargar info
                numHabitacion.setText(String.valueOf(h.getNumHabitacion()));
                descripcion.setText(h.getDescripcion());
                precio.setText(String.valueOf(h.getPrecio()));
                cantPersonas.setText(String.valueOf(h.getCantidadPersonas()));
              
                NodoExtra extra = h.getListaExtras().getPrimero();
                String extrasHabitacion = "";
                while (extra != null) {
                    extrasHabitacion += extra.getExtra().getDescripcion() + ",";
                    extra = extra.getSiguiente();
                }
                extras.setText(quitarUltimoCaracter(extrasHabitacion));

                //cargar imagenes
                for (String s : h.getImagenesNombres()) {

                    JLabel jlabelpic = new JLabel(new ImageIcon(new ImageIcon(Globales.rutaGeneral + "\\img\\habitaciones\\" + s).getImage().getScaledInstance(150, 150, Image.SCALE_DEFAULT)));
                    panelImagenes.add(jlabelpic);
                   
                }
                imagenesNombre = h.getImagenesNombres();
              
                imagenes = new File[imagenesNombre.length];
                int i = 0;
                for (String s : imagenesNombre) {
                    imagenes[i] = new File(Globales.rutaGeneral + "\\img\\habitaciones\\" + s);
                    i++;

                }

                botonAnterior.setEnabled(false);
                botonSiguiente.setEnabled(false);
            }

        } else {

            cargarListaHabitaciones();
            botonAnterior.setEnabled(true);
            botonSiguiente.setEnabled(true);
            filtroCedula.setText("");
        }

        

    }//GEN-LAST:event_activarFiltroHabitacionActionPerformed

