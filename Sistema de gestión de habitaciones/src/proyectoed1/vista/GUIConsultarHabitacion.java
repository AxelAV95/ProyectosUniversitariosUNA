    public void cargarListaHabitaciones() {
        
        actual = Datos.getListaHabitaciones().getPrimero();
        numHabitacion.setText(String.valueOf(actual.getHabitacion().getNumHabitacion()));
        descripcion.setText(actual.getHabitacion().getDescripcion());
        precio.setText(String.valueOf(Math.round(actual.getHabitacion().getPrecio())));
        cantPersonas.setText(String.valueOf(actual.getHabitacion().getCantidadPersonas()));
       
        NodoExtra extra = actual.getHabitacion().getListaExtras().getPrimero();
        
        
        
        String extrasHabitacion = "";
        while (extra != null) {
            extrasHabitacion += extra.getExtra().getDescripcion() + ",";
            extra = extra.getSiguiente();
        }
        extras.setText(quitarUltimoCaracter(extrasHabitacion));

        
        for (String s : actual.getHabitacion().getImagenesNombres()) {

            JLabel jlabelpic = new JLabel(new ImageIcon(new ImageIcon(Globales.rutaGeneral + "\\img\\habitaciones\\" + s).getImage().getScaledInstance(150, 150, Image.SCALE_SMOOTH)));
            panelImagenes.add(jlabelpic);
            
        }
        imagenesNombre = actual.getHabitacion().getImagenesNombres();
       
        imagenes = new File[imagenesNombre.length];
        int i = 0;
        for (String s : imagenesNombre) {
            imagenes[i] = new File(Globales.rutaGeneral + "\\img\\habitaciones\\" + s);
            i++;

        }

        revalidate();
        repaint();
    }

