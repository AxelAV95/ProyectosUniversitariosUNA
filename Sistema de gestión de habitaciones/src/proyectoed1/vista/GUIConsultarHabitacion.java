    public void cargarComboExtras() {

        NodoExtra ne = Datos.getListaExtras().getPrimero();
        while (ne != null) {
            filtroExtras.addItem(ne.getExtra().getCodigo() + "-" + ne.getExtra().getDescripcion());
            ne = ne.getSiguiente();
        }

    }

